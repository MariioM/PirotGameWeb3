// Actualizar la función de escuchar eventos de rifa
export const listenToRaffleEvents = (contract: Contract, callback: (result: any) => void) => {
  // Escuchar evento de entrada a la rifa
  const entryFilter = contract.filters.RaffleEntered()
  contract.on(entryFilter, (user, amount, event) => {
    callback({
      type: "entry",
      user,
      amount: amount.toString(),
      transactionHash: event.log.transactionHash,
    })
  })

  // Escuchar evento de ganador de la rifa
  const winnerFilter = contract.filters.RaffleWinnerChosen()
  contract.on(winnerFilter, (winner, reward, event) => {
    callback({
      type: "winner",
      winner,
      reward: reward.toString(),
      transactionHash: event.log.transactionHash,
    })
  })

  // Retornar una función para dejar de escuchar
  return () => {
    contract.removeAllListeners(entryFilter)
    contract.removeAllListeners(winnerFilter)
  }
}

// Función para obtener información de la rifa
export const getRaffleInfo = async (contract: Contract) => {
  try {
    const raffleEndTimestamp = await contract.raffleEndTimestamp()
    const rafflePool = await contract.rafflePool()

    return {
      endTimestamp: raffleEndTimestamp.toString(),
      pool: rafflePool.toString(),
    }
  } catch (error) {
    console.error("Error getting raffle info:", error)
    return {
      endTimestamp: "0",
      pool: "0",
    }
  }
}

// Función para obtener la entrada de un usuario en la rifa
export const getUserRaffleEntry = async (contract: Contract, userAddress: string) => {
  try {
    const entry = await contract.raffleEntries(userAddress)
    return entry.toString()
  } catch (error) {
    console.error("Error getting user raffle entry:", error)
    return "0"
  }
}

