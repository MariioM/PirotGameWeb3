// Función para escuchar eventos de CoinFlip
export const listenToCoinFlipEvents = (contract: Contract, userAddress: string, callback: (result: any) => void) => {
  const filter = contract.filters.CoinFlipResult(userAddress)
  contract.on(filter, (user, guessHeads, result, reward, event) => {
    callback({
      user,
      guessHeads,
      result,
      reward: reward.toString(),
      transactionHash: event.log.transactionHash,
    })
  })

  // Retornar una función para dejar de escuchar
  return () => {
    contract.removeAllListeners(filter)
  }
}

